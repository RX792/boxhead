class block 
{
public:
	int x, y, type;
	Box* box;

	block(int _x, int _y, int _type) 
	{

		x = _x;
		y = _y;
		type = _type;

		box = new Box(x,0,y);
		if (type == 2)
			box->set_size(1, 3, 1);

		else
			box->set_size(1, 1, 1);
		
		box->set_color(1.0, 0, 0);

	}

	void draw() 
	{
		box->draw();
	}
};

